#include <stdio.h>
int main()
{
    int integerVar;
    float floatVar;
    char charVar;

    printf("Size of int: %lu bytes\n",sizeof(int));
    print("Size of float: %lu bytes\n",sizeof(float));
    print("Size of char: %lu bytes\n",sizeof(char));

    printf("Size of integerVar: %lu bytes\n",sizeof(integerVar));
    printf("Size of floatVar: %lu bytes\n",sizeof(floatVar));
    printf("Size of charVar: %lu bytes\n",sizeof(charVar));

    return 0;

}